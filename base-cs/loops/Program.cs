//while o mientras
int i = 0;
while (i <= 10)
{
    Console.WriteLine(i);
    i++;
}

//for o para
int j = 0;
for (j=0; j<10; j++)
{
    Console.WriteLine(j);
}

//foreach o para cada
var lista = new List<string> { "Andrés", "Dross", "Gota" };
foreach (var elemento in lista)
{
    Console.WriteLine(elemento);
}